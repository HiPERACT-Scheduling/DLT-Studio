//-----------------------------------------------------------------------------------------------------------
//	Time CurrentDateTime
//-----------------------------------------------------------------------------------------------------------

std::string Time::CurrentDateTime()
{
	t = time(0);
    now = localtime(&t);

    strftime(buf, sizeof(buf), "%Y-%m-%d %X", now);

    return buf;
}

//-----------------------------------------------------------------------------------------------------------
//	Time CurrentDateTime
//-----------------------------------------------------------------------------------------------------------

std::string Time::CurrentDateTimeShifted(int x)
{
	t = time(0);
	t += 60 * x;
    now = localtime(&t);

    strftime(buf, sizeof(buf), "%Y-%m-%d %X", now);

    return buf;
}

//-----------------------------------------------------------------------------------------------------------
//	Time GetYear
//-----------------------------------------------------------------------------------------------------------

int Time::GetYear()
{
	t = time(0);   // get time now
    now = localtime( & t );

	return (now->tm_year + 1900);
}

