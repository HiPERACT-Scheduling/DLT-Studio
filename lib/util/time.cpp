//-----------------------------------------------------------------------------------------------------------
//	Time StartDuration
//-----------------------------------------------------------------------------------------------------------

std::chrono::high_resolution_clock::time_point Time::StartDuration()
{
	return std::chrono::high_resolution_clock::now();
}

//-----------------------------------------------------------------------------------------------------------
//	Time CalcDuration
//-----------------------------------------------------------------------------------------------------------

double Time::CalcDuration(std::chrono::high_resolution_clock::time_point start) //
{
	return (std::chrono::duration<double, std::micro>(std::chrono::high_resolution_clock::now()-start).count())/1000000;
}

//-----------------------------------------------------------------------------------------------------------
//	Time SetStartTime
//-----------------------------------------------------------------------------------------------------------

bool Time::SetStartTime() //
{
	bool result=true;

	t_start = std::chrono::high_resolution_clock::now();

	return result;
}

//-----------------------------------------------------------------------------------------------------------
//	Time SetStopTime
//-----------------------------------------------------------------------------------------------------------

bool Time::SetStopTime() //
{
	bool result=true;

	t_end = std::chrono::high_resolution_clock::now();

	return result;
}

//-----------------------------------------------------------------------------------------------------------
//	Time SetStopTime
//-----------------------------------------------------------------------------------------------------------

double Time::CalcWallTime() //
{
	return std::chrono::duration<double, std::micro>(t_end-t_start).count();
}


